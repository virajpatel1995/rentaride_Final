/** This enum represents possible values for the condition of a vehicle in the Rent-A-Ride system.
*
*/
public enum VehicleCondition {
    /**
     * the vehicle is in good, usable condition
     */
    GOOD,
    
    /**
     * the vehicle needs maintenance and is not in usable condition
     */
    NEEDSMAINTENANCE
}
